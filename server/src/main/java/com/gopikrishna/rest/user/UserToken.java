package com.gopikrishna.rest.user;

public class UserToken {
	String token;
	String username;
	
	public UserToken() {
	}

	public String getToken() {
		return token;
	}

	public String getUsername() {
		return username;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public void setUsername(String username) {
		this.username = username;
	}

}
